using Mutty.CodeHelpers;

namespace Mutty.Templates;

/// <summary>
/// A template that generates the mutable generation attribute.
/// </summary>
public class MutableGenerationAttributeTemplate : IndentedCodeBuilder
{
    /// <summary>
    /// Generates the code for the mutable generation attribute.
    /// </summary>
    /// <returns>The generated code.</returns>
    public string GenerateCode()
    {
        CommentAutoGenerated();
        Line("namespace Mutty");
        Braces(() =>
        {
            Summary("Indicates that the class is mutable and can be generated by the Mutty generator.");
            Line("[AttributeUsage(AttributeTargets.Class, Inherited = false)]");
            Line("public class MutableGenerationAttribute : Attribute");
            Braces(() =>
            {
                // Empty constructor
                Summary("Initializes a new instance of the <see cref=\"MutableGenerationAttribute\" /> class.");
                Line("public MutableGenerationAttribute() { }");
            });
        });

        return ToString();
    }
}
